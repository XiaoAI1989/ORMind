def scenario_fallback_visibility():
    """The opt-in fallback must be loud: a stderr warning plus per-model call
    counts; without a fallback configured the primary error must propagate."""
    import io
    from utils.llm import OpenAICompatibleLLM, UsageTracker

    original_request = OpenAICompatibleLLM._request

    def flaky_request(self, model, prompt):
        if model == "primary-model":
            raise RuntimeError("simulated provider error")
        return SimpleNamespace(
            choices=[SimpleNamespace(message=SimpleNamespace(content="ok"))],
            usage=SimpleNamespace(prompt_tokens=1, completion_tokens=1),
            model=model,
        )

    OpenAICompatibleLLM._request = flaky_request
    try:
        tracker = UsageTracker()
        llm = OpenAICompatibleLLM("primary-model", fallback_model="fallback-model", usage_tracker=tracker)
        captured = io.StringIO()
        original_stderr = sys.stderr
        sys.stderr = captured
        try:
            response = llm.invoke("hello")
        finally:
            sys.stderr = original_stderr
        assert response.content == "ok"
        assert tracker.snapshot()["calls_by_model"] == {"fallback-model": 1}, tracker.snapshot()
        assert "fallback" in captured.getvalue().lower(), "fallback must print a warning"

        no_fallback = OpenAICompatibleLLM("primary-model", fallback_model=None)
        try:
            no_fallback.invoke("hello")
            raise AssertionError("primary error must propagate when no fallback is configured")
        except RuntimeError:
            pass
    finally:
        OpenAICompatibleLLM._request = original_request
    print("PASS fallback_visibility")


