"""Offline regression tests for the ORMind pipelines.

Runs without any API key: the LLM transport is replaced by a scripted
stub, so every control-flow path (Algorithm 1, syntax repair,
counterfactual revision, every Table 2 ablation flag, the standard
baseline, and extended mode) can be exercised deterministically.

Two invariants matter most:

* Data hygiene: the ground-truth objective value must never appear in any
  prompt sent to the model (scenario_no_label_leakage).
* Mechanism causality: with the same wrong program, a flagging
  counterfactual checker must change the final output and a clean checker
  must not (scenario_counterfactual_causality) — i.e. the paper's core
  mechanism is wired end to end, not a no-op.

Also covered: grading flags that deviate from the published protocol
(accept_infeasible, tolerance overrides) and their defaults, the prob_203
data erratum, empty-input boundaries, cross-problem memory isolation,
fallback-model visibility, and the paper-default runtime configuration.

Usage:
    python tests/test_offline_pipeline.py
"""

