def scenario_counterfactual_causality():
    """Causality regression for the counterfactual loop: with the SAME wrong
    program, a flagging checker must change the final output (and its message
    must reach the revision prompt verbatim), while a clean checker must
    leave the wrong output untouched. This proves the counterfactual loop is
    causally wired, not a no-op."""
    problem, samples, inputs = load_lpwp()
    marker = "CF-MARKER-SLED-DOG-CONSTRAINT"
    checker_flag = (
        "def counterfactual_solution_analysis(obj, var1, var2):\n"
        "    return {\n"
        f'        "M1": {{"modification_needed": True, "suggestion": "{marker}"}},\n'
        '        "solution_valid_without_changes": False,\n'
        "    }"
    )

    run_dir = fresh_dir("causality_flag")
    routes = base_routes(PROB0_WRONG_FOR_CF, checker=checker_flag)
    routes[M_SUPERVISOR_BWD] = PROB0_GOOD
    set_routes(routes)
    _, output_flagged, stats_flagged = make_pipeline().solve("prob_0", problem, LPWP_ATTENTION, inputs, run_dir)
    assert output_flagged == (3000.0, 0, 10), output_flagged
    assert stats_flagged["counterfactual_status"] == "discrepancy", stats_flagged
    backward = [p for p in PROMPT_LOG if M_SUPERVISOR_BWD in p]
    assert len(backward) == 1 and marker in backward[0], "checker verdict must reach the revision prompt verbatim"

    run_dir = fresh_dir("causality_clean")
