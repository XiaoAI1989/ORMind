def scenario_paper_happy_path():
    run_dir = fresh_dir("paper_happy")
    problem, samples, inputs = load_lpwp()
    set_routes(base_routes(PROB0_GOOD))

    pipeline = make_pipeline()
    code, output, stats = pipeline.solve("prob_0", problem, LPWP_ATTENTION, inputs, run_dir)

    assert output == (3000.0, 0, 10), output
    assert stats["usage"]["llm_calls"] == 5, stats  # encoder, formalize, compile, supervise, checker
    assert stats["error_repairs"] == 0
    assert not stats["counterfactual_triggered"]

    write_generated_code(code)
    result = grade("prob_0", samples, os.path.join(run_dir, "prob_0_test_log.txt"))
    assert result == Result.ACCEPT, result

    trace_path = os.path.join(run_dir, "prob_0_ormind_trace.json")
    assert os.path.exists(trace_path)
    with open(trace_path, "r", encoding="utf8") as handle:
        trace = json.load(handle)
    assert [c["expert"] for c in trace["comments"]] == [
        "Semantic Encoder", "Formalization Thinking", "Executive Compiler",
    ]
    assert trace["counterfactual"]["status"] == "clean"
    print("PASS paper_happy_path")


