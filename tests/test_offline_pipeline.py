# Scripted LLM stub
# ----------------------------------------------------------------------
PROMPT_LOG = []
ROUTES = []


def fake_request(self, model, prompt):
    PROMPT_LOG.append(prompt)
    for marker, responses in ROUTES:
        if marker in prompt:
            text = responses.pop(0) if len(responses) > 1 else responses[0]
            return SimpleNamespace(
                choices=[SimpleNamespace(message=SimpleNamespace(content=text))],
                usage=SimpleNamespace(
                    prompt_tokens=max(1, len(prompt) // 4),
                    completion_tokens=max(1, len(text) // 4),
                ),
                model=model,
            )
    raise AssertionError("No scripted route matches this prompt:\n" + prompt[:500])


llm_module.OpenAICompatibleLLM._request = fake_request


def set_routes(route_map):
    PROMPT_LOG.clear()
    ROUTES.clear()
    for marker, responses in route_map.items():
        ROUTES.append((marker, list(responses) if isinstance(responses, list) else [responses]))


def fresh_dir(name):
    path = os.path.join(TEST_DIR, name)
    shutil.rmtree(path, ignore_errors=True)
    os.makedirs(path, exist_ok=True)
    return path


def grade(problem_name, samples, log_path):
    with open(log_path, "w", encoding="utf8") as handle:
        return test_generated_code(problem_name, samples, handle)


