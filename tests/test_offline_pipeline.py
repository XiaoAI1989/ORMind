def base_routes(supervisor_fwd, checker=CHECKER_CLEAN):
    return {
        M_ENCODER: ENCODER_JSON,
        M_FORMALIZE: FORMALIZE_JSON,
        M_COMPILER: COMPILER_TEXT,
        M_SUPERVISOR_FWD: supervisor_fwd,
        M_CHECKER: checker,
    }


def make_pipeline(**config_kwargs):
    return ORMindPipeline(model="stub-model", config=PipelineConfig(**config_kwargs))


def load_lpwp():
    problem = read_problem("LPWP", "prob_0")
    samples = read_test_samples("LPWP", "prob_0")
    inputs = [sample["input"] for sample in samples]
    return problem, samples, inputs


